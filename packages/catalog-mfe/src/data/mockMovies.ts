// Función helper para buscar películas
export const searchMovies = (query: string, movies: Movie[] = mockMovies): Movie[] => {
  if (!query.trim()) return movies;
  
  const lowercaseQuery = query.toLowerCase();
  
  return movies.filter(movie => 
    movie.title.toLowerCase().includes(lowercaseQuery) ||
    movie.description.toLowerCase().includes(lowercaseQuery) ||
    movie.genres.some(genre => genre.toLowerCase().includes(lowercaseQuery))
  );
};

// Función helper para filtrar por categoría
export const filterByCategory = (category: string, movies: Movie[] = mockMovies): Movie[] => {
  return movies.filter(movie => movie.category === category);
};

// Función helper para filtrar por género
export const filterByGenre = (genre: string, movies: Movie[] = mockMovies): Movie[] => {
  return movies.filter(movie => 
    movie.genres.some(g => g.toLowerCase() === genre.toLowerCase())
  );
};

// Función helper para filtrar por calificación
export const filterByRating = (minRating: number, movies: Movie[] = mockMovies): Movie[] => {
  return movies.filter(movie => movie.rating >= minRating);
};